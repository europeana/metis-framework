package eu.europeana.enrichment.tagger.rules;

/**
 * @author Borys Omelayenko
 */
public class PairOfStrings {
	private String first;
	private String last;

	public String getFirst() {
		return first;
	}

	public String getLast() {
		return last;
	}

	public PairOfStrings(String first, String last) {
		super();
		this.first = first;
		this.last = last;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PairOfStrings other = (PairOfStrings) obj;
		if (first == null) {
			if (other.first != null)
				return false;
		} else if (!first.equals(other.first))
			return false;
		if (last == null) {
			if (other.last != null)
				return false;
		} else if (!last.equals(other.last))
			return false;
		return true;
	}

}